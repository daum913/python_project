# 아래 모양을 출력하세요.
#  *
# ***
#  *
n = 4
for i in range (1,n) :
    print("#"*(n-i),"*"*(2*i-1),"#"*(n-i),sep="")
for i in range (n,0,-1) :
    print("#"*(n-i),"*"*(2*i-1),"#"*(n-i),sep="")

n = 2
for i in range (1,n) :
    print(" "*(n-i),"*"*(2*i-1)," "*(n-i),sep="")
